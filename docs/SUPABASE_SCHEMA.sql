drop policy if exists "customer_addresses owner or admin" on public.customer_addresses;
create policy "customer_addresses owner or admin"
on public.customer_addresses for all
to authenticated
using (
  public.is_staff()
  or exists (
    select 1 from public.customers c
    where c.id = customer_addresses.customer_id
      and c.auth_user_id = auth.uid()
  )
)
with check (
  public.is_staff()
  or exists (
    select 1 from public.customers c
    where c.id = customer_addresses.customer_id
      and c.auth_user_id = auth.uid()
  )
);

