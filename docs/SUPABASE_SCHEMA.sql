-- ============================================================
-- Timestamps helper
-- ============================================================
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

-- ============================================================
-- Admin identity/profile
-- auth.users is managed by Supabase Auth.
-- profiles links app roles to authenticated users.
-- ============================================================
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text not null default '',
  role public.user_role not null default 'staff',
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create trigger trg_profiles_updated_at
before update on public.profiles
for each row execute procedure public.set_updated_at();

-- ============================================================
-- Media assets (replaces Payload media collection)
-- Store signed/public URL metadata for your admin UI.
-- ============================================================
create table if not exists public.media_assets (
  id bigint generated by default as identity primary key,
  bucket text not null default 'product-images',
  storage_path text not null unique,
  public_url text,
  alt_text text,
  width integer,
  height integer,
  created_by uuid references public.profiles(id),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create trigger trg_media_assets_updated_at
before update on public.media_assets
for each row execute procedure public.set_updated_at();

-- ============================================================
-- Categories
-- ============================================================
create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null,
  name_ar text,
  slug text not null unique,
