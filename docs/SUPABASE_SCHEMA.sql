create trigger trg_categories_updated_at
before update on public.categories
for each row execute procedure public.set_updated_at();

-- ============================================================
-- Products
-- ============================================================
create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  name_ar text,
  slug text not null unique,
  category_id bigint not null references public.categories(id) on delete restrict,
  description text,
  description_ar text,
  price_aed numeric(10,2) not null check (price_aed >= 0),
  compare_at_price_aed numeric(10,2) check (compare_at_price_aed is null or compare_at_price_aed >= 0),
  unit public.product_unit not null default 'kg',
  stock integer not null default 100 check (stock >= 0),
  is_active boolean not null default true,
  is_featured boolean not null default false,
  is_organic boolean not null default false,
  origin text,
  emoji text,
  image_url text,
  tags text[] not null default '{}',
  created_by uuid references public.profiles(id),
  updated_by uuid references public.profiles(id),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create index if not exists idx_products_slug on public.products(slug);
create index if not exists idx_products_category_id on public.products(category_id);
create index if not exists idx_products_is_active on public.products(is_active);
create index if not exists idx_products_is_featured on public.products(is_featured);
create index if not exists idx_products_created_at on public.products(created_at desc);

create trigger trg_products_updated_at
before update on public.products
for each row execute procedure public.set_updated_at();

-- If you need multiple images per product, this replaces Payload "images" array.
create table if not exists public.product_images (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products(id) on delete cascade,
  media_asset_id bigint references public.media_assets(id) on delete set null,
  image_url text,
  sort_order integer not null default 0,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  check (media_asset_id is not null or image_url is not null)
);

create index if not exists idx_product_images_product_id on public.product_images(product_id);
create index if not exists idx_product_images_sort_order on public.product_images(sort_order);

create trigger trg_product_images_updated_at
before update on public.product_images
for each row execute procedure public.set_updated_at();

